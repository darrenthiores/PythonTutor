#Operator Penugasan

#Operator tambah sama dengan (+=)
a = 5
a += 1
print (a)

#Operator kurang sama dengan (-)
b = 5
b -= 1
print (b)

#Operator kali sama dengan (*=)
c = 5
c *= 2
print (c)

#Operator bagi sama dengan (/=)
d = 5
d /= 2
print (d)

#Operator sisa sama dengan (%=)
e = 5
d %= 2
print (e)

#Operator pangkat sama dengan (**=)
f = 5
f **= 2
print (f)

#Operator bagi bulat sama dengan (//=)
g = 5
g //= 2
print (g)

#Operator Logical
laki = False
perempuan = True

#and
Sama = laki and perempuan
print (Sama)

#or
atau = laki or perempuan
print (atau)

#not
benar = not laki
salah = not perempuan

print (benar, salah)