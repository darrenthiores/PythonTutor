#Operator artimatika
x = 10
y = 3

#Operator tambah (+)
jumlah = x + y
print (jumlah)

#Operator kurang (-)
kurang = x - y
print (kurang)

#Operator perkalian (*)
kali = x * y
print (kali)

#Operator pembagian (/)
bagi = x / y
print (bagi)

#Operator sisa bagi (%)
sisa = x % y
print (sisa)

#Operator pangkat (**)
pangkat = x**y
print (pangkat)

#Operator pembagian bulat (//)
pembagianBulat = x // y
print (pembagianBulat)

