#Mengkonversi tipe data pada python
#mengubah tipe data menjadi integer --> int()
p = '10'
l = 12.5

print (int(p))
print (int(l))

#mengubah tipe data menjadi tipe float --> float()
x = '10'
y = 17

print (float(x))
print (float(y))

#mengubah tipe data menjadi string --> str()
a = 17.2
b = 19
c = True

print (str(a))
print (str(b))
print (str(c))

